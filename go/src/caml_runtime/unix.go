func Unix_isatty(fd_ V) V {
	fd := fd_.(int)
	file := os.NewFile(uintptr(fd), "(Unix_isatty test file)")
	fileInfo, err := file.Stat()
	Caml_fail_if_err(err)
	return (fileInfo.Mode() & os.ModeCharDevice) != 0
}

